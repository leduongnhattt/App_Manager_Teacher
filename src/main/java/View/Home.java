    public void DeleteCBGV() {
        ImageIcon icon = new ImageIcon("Pictogrammers-Material-Account.24");
        DefaultTableModel tb = (DefaultTableModel) tblthongke.getModel();
        if (tb.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Danh sách đang rỗng!");
            return;
        }
        String maSo = (String) JOptionPane.showInputDialog(
                null,
                "Nhập mã số giáo viên cần xóa:",
                "Tìm Kiếm",
                JOptionPane.INFORMATION_MESSAGE,
                icon,
                null,
                ""
        );
        if (list_cbgv.check() == true) {
            JOptionPane.showMessageDialog(rootPane, "Danh sách đang rỗng!", "Cảnh báo", 2);
            return;
        }
        CBGV cbgv = list_cbgv.kiemTraTonTai(maSo);
        if (cbgv == null) {
            JOptionPane.showMessageDialog(rootPane, "Không tồn tại giáo viên này!");
        } else {
            int option = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa giáo viên này không?", "Warning", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                int len = tb.getRowCount();
                for (int i = len - 1; i >= 0; i--) {
                    String id = (String) tb.getValueAt(i, 0);
                    if (maSo.equals(id)) {
                        list_cbgv.deleteCBGV(maSo);
                        tb.removeRow(i);
                        JOptionPane.showMessageDialog(rootPane, "Xóa thành công!");
                    }
                }
            }
        }
    }

